SELECT * FROM FORECAST.MAINT_FEE_EVENT;

SELECT 
COUNT(CASE WHEN b.ENTRY_CD IN 
      ('M1551','M170','M173','M183','M2551','M273','M283','M3551') 
      THEN 1 ELSE NULL END) "1st_renewal",
COUNT(CASE WHEN b.ENTRY_CD IN 
      ('M1552','M171','M174','M184','M2552','M274','M284','M3552') 
      THEN 1 ELSE NULL END) "2nd_renewal",
COUNT(CASE WHEN b.ENTRY_CD IN 
      ('M1553','M172','M175','M185','M2553','M275','M285','M3553') 
      THEN 1 ELSE NULL END) "3rd_renewal"
FROM FORECAST.MAINT_FEE_EVENT b
WHERE b.ENTRY_DT Between TO_DATE('10-01-2016', 'MM-DD-YYYY')
AND TO_DATE('09-30-2017', 'MM-DD-YYYY');